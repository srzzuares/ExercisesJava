    private void addProduct() {
        idList = id_clave.getText();
        clave = Integer.parseInt(id_clave.getText());
        nombre = id_Producto.getText();
        descripcion = id_Desc_Producto.getText();
        costo = Double.parseDouble(id_CostoUni.getText());
        cantidad = Double.parseDouble(id_Cantidad.getText());
        precioTotal = Double.parseDouble(id_PrecioTotal.getText());
        
        producto addData = new producto(clave,nombre,descripcion,costo,cantidad,precioTotal,idList);
        desing.addElement(addData);
        jList1.setModel(desing);
    }

