    private void DropProduct() {
        if(jList1.getSelectedIndex() != -1){
           desing.removeElementAt(jList1.getSelectedIndex());
           jList1.setModel(desing);
        }  else {
            JOptionPane.showMessageDialog(null,"Debe Seleccionar un Elemento",
                    "Error",JOptionPane.ERROR_MESSAGE);
        }
    }
        
    

    private void reload() {
        int index = jList1.getSelectedIndex();
        
        producto addList = (producto)desing.getElementAt(index);
        id_clave.setText(String.valueOf(addList.getClave()));
        id_Producto.setText(addList.getNombre());
        id_Desc_Producto.setText(addList.getDescripcion());
        id_CostoUni.setText(String.valueOf(addList.getCosto()));
        id_Cantidad.setText(String.valueOf(addList.getCantidad()));
        id_PrecioTotal.setText(String.valueOf(addList.getPrecioTotal()));
        
        subprecio = addList.getPrecioTotal();
        id__SubTotal.setText(Double.toString(subprecio));
        iva = subprecio * .16;
        id__Iva.setText(Double.toString(iva));
        originPrecio = iva + subprecio;
        id__Total.setText(Double.toString(originPrecio));
    }


