        String num=jTextField1.getText();
        int valnum= Integer.parseInt(num);
        
        int t, resultado;
        resultado = 1;
        for(t=1; t<=valnum; t++){
            resultado *= t;
            JOptionPane.showMessageDialog(null,"El Factorial de: " +valnum," es " +resultado,JOptionPane.INFORMATION_MESSAGE);
        }
        
