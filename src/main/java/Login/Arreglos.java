    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String semana[]={"Lunes", "Martes", "Miercoles", "Jueves", "Viernes", "Sabado", "Domingo","Hola Humano"};
        
        int tamanioArray = semana.length;
        JOptionPane.showMessageDialog(null,"Longitud del arreglo: " + tamanioArray,"longitud",JOptionPane.INFORMATION_MESSAGE);
        
        for(int i = 0; i <tamanioArray; i++){
            JOptionPane.showMessageDialog(null,"Longitud del arreglo: " + semana[i] + " Con su pocisión: " +i,"longitud",JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        String b = JOptionPane.showInputDialog(null, "Introduce el valor de la tabla de multiplicar","Tabla",JOptionPane.INFORMATION_MESSAGE);
        int multiplicacion= Integer.parseInt(b);
        int res = 0;
        for(int i = 1; i <= 10; i++){
            res = i *multiplicacion;
            JOptionPane.showMessageDialog(null,multiplicacion + " X " + i + " = " + res,"Tabla de Multiplicar",JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton2MouseClicked

