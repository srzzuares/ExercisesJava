    public void AgregarNombre(){
          String nombre = jTextField1.getText(); 
          model.addElement(nombre);
          jList1.setModel(model); //Llenado del elemento
          jTextField1.setText("");
          jLabel2.setText("Datos Agregados");
        }
    
    public void EliminarNombre(int indice){
        if(indice >= 0){
            model.removeElementAt(indice);
            jLabel2.setText("Se elimino un elemento en la posici√≥n " +indice);
        } else {
            JOptionPane.showMessageDialog(null,"Debe Seleccionar un Elemento",
                    "Error",JOptionPane.ERROR_MESSAGE);
            jLabel2.setText("No Selecciono ningun Elemento");
        }
    }
    private void Limpiar() {
        model.clear();
    }
